function combinationCounter(n, r){
	var n1 = 1;
	var r1 = 1;
	var nr1 = 1;
	for(var i = 1; i <=n; i++){
		n1 *= i;
	}
	for(var i = 1; i <= r; i++){
		r1 *= i;
	}
	for(var i = 2; i <= n - r; i++){
		nr1 *= i;
	}
	
	return n1 / (r1 * nr1);
}
