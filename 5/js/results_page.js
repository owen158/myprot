 function combine(a, n, repeatAllowed) {
		a.sort();
		var b = [];// 辅助空间，保存待输出组合数
		var result = [];
		getCombination(a, n, 0, b, 0, result, repeatAllowed);

		return result;
}

function getCombination(a, n, begin, b, index, result, repeatAllowed) {
	if (n == 0) {// 如果够n个数了，输出b数组
		var temp = [];
		for (var i = 0; i < index; i++) {
			temp[i] = b[i];
		}
		result.push(temp);
		return;
	}

		var i = repeatAllowed ? 0 : begin;
	for (i; i < a.length; i++) {

		if (index == 0 || a[i] >= b[index - 1]) {
		b[index] = a[i];
		getCombination(a, n - 1, i + 1, b, index + 1, result, repeatAllowed);
		}
	}
}

